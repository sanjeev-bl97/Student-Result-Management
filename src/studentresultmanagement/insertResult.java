    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
       
        String rollno = jTextField1.getText();
        String maths = jTextField2.getText();
        String physics = jTextField3.getText();
        String eg = jTextField4.getText();
        String java = jTextField5.getText();
        String dbms = jTextField6.getText();
        
        if(rollno.isEmpty() || maths.isEmpty() || physics.isEmpty() || eg.isEmpty() || java.isEmpty() || dbms.isEmpty()){
        
             JOptionPane.showMessageDialog(null, "Please fill all the details");
            setVisible(false);
            new insertResult().setVisible(true);
        }
        
        else{
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Test$123");
            
            Statement st = con.createStatement();
            ResultSet rt =  st.executeQuery("select * from student where rollno = '"+rollno+"' " );
            
            if(rt.next()){
              st.executeUpdate("insert into result(rollno,maths,physics,eg,java,dbms) values('"+rollno+"' ,'"+maths+"' ,'"+physics+"' ,'"+eg+"' ,'"+java+"' ,'"+dbms+"')");
           
              JOptionPane.showMessageDialog(null, "Result Saved Successfully");
            }
            else
            {
             JOptionPane.showMessageDialog(null, "Student is not Registered");
            }
            
             setVisible(false);
            new insertResult().setVisible(true);
        }
        
        catch(Exception e){
        JOptionPane.showMessageDialog(null, e.toString());
        }
        
        }
    }//GEN-LAST:event_jButton6ActionPerformed

